// }

/*Третьим аргументом также может быть строка. В этом случае строка будет использоваться для отступа вместо ряда 
пробелов.*/
console.log(JSON.stringify(obj08, null, '↕↕↕↕'));
// {
// ↕↕↕↕"b": "b",
// ↕↕↕↕"c": [
// ↕↕↕↕↕↕↕↕{
